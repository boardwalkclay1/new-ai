# ---------- DB SCHEMA ----------
def create_db_schema(name: str, schema: str) -> str:
    sname = safe_name(name)
    db_dir = os.path.join(FILES_DIR, "db")
    os.makedirs(db_dir, exist_ok=True)
    path = os.path.join(db_dir, f"{sname}.sql")
    with open(path, "w", encoding="utf-8") as f:
        f.write(schema)
    return path

# ---------- R2 CONFIG ----------
def create_r2_config(bucket_name: str) -> str:
    sname = safe_name(bucket_name)
    cf_dir = os.path.join(FILES_DIR, "cloudflare")
    os.makedirs(cf_dir, exist_ok=True)
    path = os.path.join(cf_dir, "r2.toml")
    config = f"""[[r2_buckets]]
binding = "{sname}"
bucket_name = "{sname}"
preview_bucket_name = "{sname}-preview"
"""
    with open(path, "w", encoding="utf-8") as f:
        f.write(config)
    return path

# ---------- API BACKEND ----------
def generate_api_backend(project: str, spec: str) -> str:
    sproj = safe_name(project)
    base = os.path.join(FILES_DIR, sproj, "backend")
    os.makedirs(base, exist_ok=True)

    spec_path = os.path.join(base, "api_spec.md")
    with open(spec_path, "w", encoding="utf-8") as f:
        f.write(f"# API Spec for {sproj}\n\n{spec}\n")

    server_path = os.path.join(base, "server.js")
    with open(server_path, "w", encoding="utf-8") as f:
        f.write(f"""const express = require("express");
const cors = require("cors");
const app = express();
app.use(cors());
app.use(express.json());

// API generated from spec file: api_spec.md
// Project: {sproj}

app.get("/api/health", (req, res) => {{
  res.json({{ ok: true, service: "{sproj}" }});
}});

// TODO: Implement routes described in api_spec.md

const port = process.env.PORT || 4000;
app.listen(port, () => {{
  console.log("API backend for {sproj} listening on port", port);
}});
""")
    return base

# ---------- ENDPOINTS ----------
@app.post("/api/chat", response_model=ChatResponse)
def chat(req: ChatRequest):
    last = req.messages[-1].content if req.messages else ""
    reply = f"Note recorded: {last}"
    return ChatResponse(reply=reply)

@app.post("/api/scaffold", response_model=ScaffoldResponse)
def api_scaffold(req: ScaffoldRequest):
    path = scaffold_project(req.stack, req.name)
    return ScaffoldResponse(message=f"Project scaffolded with stack '{req.stack}'", path=path)

@app.post("/api/db/schema", response_model=DbSchemaResponse)
def api_db_schema(req: DbSchemaRequest):
    path = create_db_schema(req.name, req.schema)
    return DbSchemaResponse(message="Database schema file created", path=path)

@app.post("/api/cloudflare/r2", response_model=R2Response)
def api_r2(req: R2Request):
    path = create_r2_config(req.bucket_name)
    return R2Response(message="Cloudflare R2 config created", path=path)

@app.post("/api/api/generate", response_model=ApiGenResponse)
def api_generate(req: ApiGenRequest):
    path = generate_api_backend(req.project, req.spec)
    return ApiGenResponse(message="API backend generated", path=path)

if __name__ == "__main__":
    import uvicorn
    uvicorn.run("main:app", host="0.0.0.0", port=8000, reload=True)
