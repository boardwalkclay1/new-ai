# backend/main.py
import os
import shutil
from typing import List

from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel

# ---------- CONFIG ----------
BASE_DIR = os.path.dirname(os.path.abspath(__file__))
FILES_DIR = os.path.join(BASE_DIR, "boardwalk_files")
os.makedirs(FILES_DIR, exist_ok=True)

# ---------- APP ----------
app = FastAPI()
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# ---------- MODELS ----------
class Message(BaseModel):
    role: str
    content: str

class ChatRequest(BaseModel):
    messages: List[Message]

class ChatResponse(BaseModel):
    reply: str

class ScaffoldRequest(BaseModel):
    stack: str
    name: str

class ScaffoldResponse(BaseModel):
    message: str
    path: str

class DbSchemaRequest(BaseModel):
    name: str
    schema: str

class DbSchemaResponse(BaseModel):
    message: str
    path: str

class R2Request(BaseModel):
    bucket_name: str

class R2Response(BaseModel):
    message: str
    path: str

class ApiGenRequest(BaseModel):
    project: str
    spec: str

class ApiGenResponse(BaseModel):
    message: str
    path: str

# ---------- HELPERS ----------
def safe_name(name: str) -> str:
    return name.replace("..", "_").strip().replace(" ", "-")

